export const nonQualifiedTeams = [...nonQualifiedSeeds].sort(
  (a, b) => CONFED_ORDER[a.confederation] - CONFED_ORDER[b.confederation] || a.fifaRank - b.fifaRank,
);

export function getNonQualifiedTeam(slug: string): NonQualifiedTeam | undefined {
  return nonQualifiedSeeds.find((t) => t.slug === slug);
}

export function getNonQualifiedByConfederation(conf: Confederation): NonQualifiedTeam[] {
  return nonQualifiedSeeds.filter((t) => t.confederation === conf);
}
